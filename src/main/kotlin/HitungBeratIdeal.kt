fun main(){
    val tinggiBadan : Int = 150
    val beratBadan : Int = 47

    val beratBadanIdeal : Int = tinggiBadan - 110
    if (beratBadan <= beratBadanIdeal){
        println("kurus")
    }else if (beratBadan == beratBadanIdeal){
        println("ideal")
    }else if (beratBadan >= beratBadan)
        println("gemuk")
}